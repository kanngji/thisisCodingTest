# Balanced binary tree는 모든 노드의 두 하위 트리의 높이 차이가 최대 1 이하인 이진 트리입니다. 즉, 트리의 어느 노드에서든 왼쪽 하위 트리와 오른쪽 하위 트리의 높이 차이가 1을 넘지 않습니다. 이를 통해 트리의 깊이가 최소화되며, 이는 삽입, 삭제, 검색 연산에서 효율적인 성능을 보장합니다.

# 주요 특징:

# 높이 균형: 트리의 각 노드에 대해 왼쪽 하위 트리와 오른쪽 하위 트리의 높이 차이가 최대 1.
# 최소 깊이: 트리가 가능한 한 평평하게 유지되어 연산의 시간 복잡도가 낮아짐.
# 효율적인 연산: 균형이 잘 잡힌 트리는 일반적으로 이진 탐색 트리(BST)보다 삽입, 삭제, 검색 연산의 성능이 더 좋음.
# 예시
# 완전 이진 트리: 모든 레벨이 꽉 찬 이진 트리 (마지막 레벨을 제외하고).
# AVL 트리: 모든 노드의 두 하위 트리 높이 차이가 1 이하인 트리.
# Red-Black 트리: 노드에 색깔을 칠해 균형을 유지하는 트리.
